							</p>
						</li>
						<li>
							<span class="mb-1 block font-bold">Time to Isolation</span>
							<p class="leading-normal">
								Avg. days until an infectious person is hospitalized. Key metric for contact tracing
								efficiency.
							</p>
						</li>
						<li>
							<span class="mb-1 block font-bold">Hospital Safety (β<sub>H</sub>)</span>
							<p class="leading-normal">
								Risk of transmission within medical facilities. Controlled by PPE quality and
								protocols.
							</p>
						</li>
						<li>
							<span class="mb-1 block font-bold">Funeral Safety (β<sub>F</sub>)</span>
							<p class="leading-normal">
								Risk of transmission during burials. Reduced by Safe and Dignified Burial (SDB)
								compliance.
							</p>
						</li>
					</ul>
				</div>
				<div>
					<h4 class="mb-2 text-[10px] font-bold uppercase">Virus Characteristics</h4>
					<ul class="flex flex-col gap-4 text-xs leading-tight opacity-80">
						<li>
							<span class="mb-1 block font-bold">Incubation Period</span>
							<p class="leading-normal">
								Time from exposure to symptom onset. For BDBV 2026, baseline is 7.0 days.
							</p>
						</li>
						<li>
							<span class="mb-1 block font-bold">Mortality Rates</span>
							<p class="leading-normal">
								The daily probability of death. Significantly higher without supportive hospital
								care.
							</p>
						</li>
						<li>
							<span class="mb-1 block font-bold">Initial Seeds (E<sub>0</sub>, I<sub>0</sub>)</span>
